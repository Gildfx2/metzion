package com.example.pre_alpha.notifications;

public class Token {
    // Attribute of token
    String token;

    public Token(){}

    public Token(String token){
        this.token=token;
    } // Constructor

    // Getter and Setter
    public String getToken() {
        return token;
    }
    public void setToken(String token) {
        this.token = token;
    }
}
